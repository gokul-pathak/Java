public class Resulting_data_types {
    public static void main(String[] args) {
        byte x = 5;
        int y = 6;
        short z = 8;
        int a = 45 + 8;
        float b = 6.54f + x;
        System.out.println(b);

        // Increment and Decrement Operators
        int i = 56;
        //int b = i++  first b is assigned i then i is incremented
        int j = 67;
        int p = ++j; //first j is incremented then p is assigned j (68)
        System.out.println(i++); // first use and increase
        System.out.println(i);
        System.out.println(++i); // increase and use
        System.out.println(i);
        char ab= 'b';
        System.out.println(ab++);
        System.out.println(ab);

    }
}
